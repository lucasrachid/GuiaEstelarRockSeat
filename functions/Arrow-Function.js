// Arrow Function

//Function sem um nome especifico, apenas declarada, pode utilizar abreviacao da seguinte forma.

// Geralmente a Arrow function é utilizada em uma constante, não é regra.

const sayMyName = () => {
  console.log(`Rachid`);
};

sayMyName();

const sayMyName2 = (name2) => {
  console.log(name2);
};

sayMyName2(`Lucas`);
